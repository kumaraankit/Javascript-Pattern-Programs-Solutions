let n=10
string=""


for (let i=1; i<=n; i++)
{
    for (let j=n; j>i; j--)
    {
      string+="  "
    }
    for (let j=1; j<=(2*i-1); j++ )
    {
        string+="* "
    }
    string+="\n"
}
for (let i=n-1; i>=1; i--)
{
    for (let j=n; j>i; j--)
    {
      string+="  "
    }
    for (let j=1; j<=(2*i-1); j++ )
    {
        string+="* "
    }
    string+="\n"
}
